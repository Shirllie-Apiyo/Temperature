
import java.util.Arrays;
public class Sort{
        public static void main(String[] args) {
            int[] arr = {5, 3, 8, 1, 2, 9, 4, 7, 6};

            // sort the array in ascending order
            Arrays.sort(arr);

            // print the sorted array
            System.out.println(Arrays.toString(arr));
        }

}
